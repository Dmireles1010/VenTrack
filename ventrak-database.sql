
-- VENDING MACHINE TABLE --
CREATE TABLE VENDING_MACHINE(
  VENDING_MACHINE_ID INT NOT NULL,
  LOCATION VARCHAR(30),
  AMOUNT_OF_ITEMS INT,
  TOTAL_MONEY DOUBLE,
  PRIMARY KEY(VENDING_MACHINE_ID)
);


-- ITEM TABLE --
CREATE TABLE ITEM(
  ITEM_ID VARCHAR(2) NOT NULL,
  NAME VARCHAR(20),
  PRODUCTION_COMPANY VARCHAR(30),
  PRICE DOUBLE,
  QUANTITY INT NOT NULL,
  VENDING_MACHINE_ID INT NOT NULL,
  PRIMARY KEY(ITEM_ID, VENDING_MACHINE_ID),
  FOREIGN KEY (VENDING_MACHINE_ID) REFERENCES VENDING_MACHINE(VENDING_MACHINE_ID)
);

CREATE TABLE PURCHASE(
    PURCHASE_NUMBER INT NOT NULL,
    DATE_OF_PURCHASE DATE,
    PRICE DOUBLE,
    ITEM_ID VARCHAR(2) NOT NULL,
    VENDING_MACHINE_ID INT NOT NULL ,
    PRIMARY KEY(PURCHASE_NUMBER),
    FOREIGN KEY (ITEM_ID, VENDING_MACHINE_ID) REFERENCES ITEM(ITEM_ID, VENDING_MACHINE_ID)
);

